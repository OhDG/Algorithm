dn = -1
arr = [0 for i in range(10001)]

for i in range(10001):
    
    if i < 10:
        dn = i + i
    elif i < 100:
        dn = i + i // 10 + i % 10
    elif i < 1000:
        dn = i + i // 100 + ( i // 10 ) % 10 + i % 10
    elif i < 10000:
        dn = i + i // 1000 + ( i // 100 ) % 10 + ( i // 10 ) % 10 + i % 10
    
    if dn < 10001:
        arr[dn] = -1

    if arr[i] != -1:
        print(i)